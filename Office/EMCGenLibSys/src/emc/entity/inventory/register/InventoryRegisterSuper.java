package emc.entity.inventory.register;

import emc.framework.EMCEntityClass;
import javax.persistence.Entity;

/**
 *
 * @author wikus
 */
@Entity
public class InventoryRegisterSuper extends EMCEntityClass {

    private String masterId;
    private String transId;
    private String batch;
    private String serial;
    private String warehouse;
    private String location;
    private String pallet;
    private String type;
    private double quantity;
    //For validation
    private double totalQty;
    private double registeredQty;

    public String getBatch() {
        return batch;
    }

    public void setBatch(String batch) {
        this.batch = batch;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getMasterId() {
        return masterId;
    }

    public void setMasterId(String masterId) {
        this.masterId = masterId;
    }

    public String getPallet() {
        return pallet;
    }

    public void setPallet(String pallet) {
        this.pallet = pallet;
    }

    public String getSerial() {
        return serial;
    }

    public void setSerial(String serial) {
        this.serial = serial;
    }

    public String getTransId() {
        return transId;
    }

    public void setTransId(String transId) {
        this.transId = transId;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWarehouse() {
        return warehouse;
    }

    public void setWarehouse(String warehouse) {
        this.warehouse = warehouse;
    }

    public double getQuantity() {
        return quantity;
    }

    public void setQuantity(double quantity) {
        this.quantity = quantity;
    }

    public double getRegisteredQty() {
        return registeredQty;
    }

    public void setRegisteredQty(double registeredQty) {
        this.registeredQty = registeredQty;
    }

    public double getTotalQty() {
        return totalQty;
    }

    public void setTotalQty(double totalQty) {
        this.totalQty = totalQty;
    }
}

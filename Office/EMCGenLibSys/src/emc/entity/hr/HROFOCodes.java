package emc.entity.hr;

import emc.datatypes.EMCDataType;
import emc.datatypes.hr.ofocode.OFOCode;
import emc.datatypes.systemwide.Description;
import emc.framework.EMCEntityClass;
import java.util.HashMap;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

/**
 *
 * @author wikus
 */
@Entity
@Table(name = "HROFOCodes", uniqueConstraints = {@UniqueConstraint(columnNames = {"ofoCode", "companyId"})})
public class HROFOCodes extends EMCEntityClass {

    private String ofoCode;
    private String description;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getOfoCode() {
        return ofoCode;
    }

    public void setOfoCode(String ofoCode) {
        this.ofoCode = ofoCode;
    }

    @Override
    protected HashMap<String, EMCDataType> buildFieldList() {
        HashMap<String, EMCDataType> toBuild = super.buildFieldList();
        toBuild.put("ofoCode", new OFOCode());
        toBuild.put("description", new Description());
        return toBuild;
    }

    @Override
    protected List<String> buildDefaultLookupFieldList() {
        List<String> toBuild = super.buildDefaultLookupFieldList();
        toBuild.add("ofoCode");
        toBuild.add("description");
        return toBuild;
    }
}

package emc.entity.hr;

import emc.datatypes.EMCDataType;
import emc.datatypes.hr.disabilitytypes.DisabilityTypes;
import emc.datatypes.systemwide.Description;
import emc.framework.EMCEntityClass;
import java.util.HashMap;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

/**
 *
 * @author wikus
 */
@Entity
@Table(name = "HRDisabilityTypes", uniqueConstraints = {@UniqueConstraint(columnNames = {"type", "companyId"})})
public class HRDisabilityTypes extends EMCEntityClass {

    private String type;
    private String description;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    protected HashMap<String, EMCDataType> buildFieldList() {
        HashMap<String, EMCDataType> toBuild = super.buildFieldList();
        toBuild.put("type", new DisabilityTypes());
        toBuild.put("description", new Description());
        return toBuild;
    }

    @Override
    protected List<String> buildDefaultLookupFieldList() {
        List<String> toBuild = super.buildDefaultLookupFieldList();
        toBuild.add("type");
        toBuild.add("description");
        return toBuild;
    }
}

package emc.entity.hr;

import emc.datatypes.EMCDataType;
import emc.datatypes.hr.disiplinarylevel.LevelId;
import emc.datatypes.systemwide.Description;
import emc.framework.EMCEntityClass;
import java.util.HashMap;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

/**
 *
 * @author wikus
 */
@Entity
@Table(name = "HRDisciplinaryLevel", uniqueConstraints = {@UniqueConstraint(columnNames = {"levelId", "companyId"})})
public class HRDisciplinaryLevel extends EMCEntityClass {

    private String levelId;
    private String description;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLevelId() {
        return levelId;
    }

    public void setLevelId(String levelId) {
        this.levelId = levelId;
    }

    @Override
    protected HashMap<String, EMCDataType> buildFieldList() {
        HashMap<String, EMCDataType> toBuild = super.buildFieldList();
        toBuild.put("levelId", new LevelId());
        toBuild.put("description", new Description());
        return toBuild;
    }

    @Override
    protected List<String> buildDefaultLookupFieldList() {
        List<String> toBuild = super.buildDefaultLookupFieldList();
        toBuild.add("levelId");
        toBuild.add("description");
        return toBuild;
    }
}

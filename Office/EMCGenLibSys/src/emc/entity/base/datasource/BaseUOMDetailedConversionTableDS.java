package emc.entity.base.datasource;

import emc.entity.base.BaseUOMDetailedConversionTable;
import emc.inventory.ItemReferenceInterface;

/**
 *
 * @author wikus
 */
public class BaseUOMDetailedConversionTableDS extends BaseUOMDetailedConversionTable implements ItemReferenceInterface {

    private String itemReference;
    private String itemDescription;
    private String unit;
    private String baseUnit;

    public BaseUOMDetailedConversionTableDS() {
        this.setDataSource(true);
    }

    public String getBaseUnit() {
        return baseUnit;
    }

    public void setBaseUnit(String baseUnit) {
        this.baseUnit = baseUnit;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getItemDescription() {
        return itemDescription;
    }

    public void setItemDescription(String itemDescription) {
        this.itemDescription = itemDescription;
    }

    public String getItemReference() {
        return itemReference;
    }

    public void setItemReference(String itemReference) {
        this.itemReference = itemReference;
    }
}

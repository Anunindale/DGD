package emc.datatypes.base.messages;

import emc.datatypes.EMCString;

/**
 * @name        Message
 *
 * @date        20 Apr 2009
 *
 * @desciption  Datatype for message field on BaseMessages.  Mandatory.
 *
 * @author      riaan
 */
public class Message extends EMCString {

    /** Creates a new instance of Message. */
    public Message() {
        this.setEmcLabel("Message");
        this.setMandatory(true);
        this.setStringSize(254);
        this.setColumnWidth(500);
        this.setLowerCaseAllowed(true);
    }
}

package emc.datatypes.developertools.bugtracking;

import emc.datatypes.EMCString;

/**
 * @Name         : BugId
 *
 * @Date         : 14 Aug 2009
 *
 * @Description  : Datatype for the bugId field on the DevBugs entity.
 *
 * @author       : riaan
 */
public class BugId extends EMCString {

    /** Creates a new instance of BugId. */
    public BugId() {
        this.setEmcLabel("Task");
        this.setStringSize(20000);
        this.setColumnWidth(264);
        setLowerCaseAllowed(true);
    }
}

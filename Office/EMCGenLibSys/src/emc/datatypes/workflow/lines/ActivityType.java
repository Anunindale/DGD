import emc.datatypes.workflow.activtytype.*;
import emc.datatypes.EMCString;
import emc.entity.workflow.WorkFlowActivityTypes;

public class ActivityType extends EMCString {

    /** Creates a new instance of ActivityType */
    public ActivityType() {
        this.setEmcLabel("Activity Type");
        this.setColumnWidth(50);
        this.setMandatory(true);
        this.setRelatedTable(WorkFlowActivityTypes.class.getName());
        this.setRelatedField("activityType");
    }
}

package emc.datatypes.workflow.activities;

import emc.datatypes.EMCString;
import emc.entity.workflow.WorkFlowActivityTypes;

/**
 *
 * @author wikus
 */
public class Type extends EMCString {

    /** Creates a new instance of Type */
    public Type() {
        this.setEmcLabel("Type");
        this.setEditable(true);
        this.setStringSize(50);
        this.setRelatedTable(WorkFlowActivityTypes.class.getName());
        this.setRelatedField("activityType");
    }
}

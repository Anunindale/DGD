package emc.datatypes.workflow.activities;

import emc.datatypes.EMCString;
import emc.entity.workflow.WorkFlowActivityGroups;

/**
 *
 * @author wikus
 */
public class ActivityGroup extends EMCString {

    /** Creates a new instance of ActivitrGroup */
    public ActivityGroup() {
        this.setEmcLabel("Activity Group");
        this.setStringSize(50);
        this.setEditable(true);
        this.setRelatedTable(WorkFlowActivityGroups.class.getName());
        this.setRelatedField("activityGroup");
    }
}

package emc.datatypes.inventory.inventoryreference;

import emc.datatypes.EMCString;
import emc.entity.inventory.InventoryReferenceType;

/**
 *
 * @author wikus
 */
public class ReferenceType extends EMCString {

    /** Creates a new instance of ReferenceType */
    public ReferenceType() {
        this.setEmcLabel("Type");
        this.setRelatedTable(InventoryReferenceType.class.getName());
        this.setRelatedField("type");
        this.setMandatory(true);
        this.setEditable(true);
    	this.setColumnWidth(45);
        this.setStringSize(10);
    }
}

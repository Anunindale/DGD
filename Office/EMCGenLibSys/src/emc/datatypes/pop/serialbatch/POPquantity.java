package emc.datatypes.pop.serialbatch;

import emc.datatypes.EMCDouble;

/**
 *
 * @author wikus
 */
public class POPquantity extends EMCDouble {

    /** Creates a new instance of POPQuantity */
    public POPquantity() {
        this.setEmcLabel("Quantity");
        this.setMandatory(true);
        this.setEditable(true);
        this.setNumberDecimalsDisplay(2);
        this.setMinValue(Double.NEGATIVE_INFINITY);
    }
}

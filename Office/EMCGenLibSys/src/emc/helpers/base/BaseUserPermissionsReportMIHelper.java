package emc.helpers.base;

/**
 *
 * This class is used to send menu item information to the server for the
 * user permissions report.
 * 
 * @author riaan
 */
public class BaseUserPermissionsReportMIHelper {

    private String menuItemLabel;
    private String formClassName;

    /** Creates a new instance of BaseUserPermissionsReportMIHelper. */
    public BaseUserPermissionsReportMIHelper() {

    }

    public String getMenuItemLabel() {
        return menuItemLabel;
    }

    public void setMenuItemLabel(String menuItemLabel) {
        this.menuItemLabel = menuItemLabel;
    }

    public String getFormClassName() {
        return formClassName;
    }

    public void setFormClassName(String formClassName) {
        this.formClassName = formClassName;
    }
}

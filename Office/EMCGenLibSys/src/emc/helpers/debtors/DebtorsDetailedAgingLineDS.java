package emc.helpers.debtors;

import java.math.BigDecimal;
import java.util.Date;

/**
 * @description : This data source is used to represent detailed aging data.
 *
 * @date        : 08 Jul 2010
 *
 * @author      : Riaan Nel
 *
 * @version     : 1.0
 */
public class DebtorsDetailedAgingLineDS {

    private String transactionType;
    private String transactionRef;
    private Date transactionDate;
    private BigDecimal amount;
    private BigDecimal balance;
    private long transRecordID;
    private Date paymentDueDate;
    private String customerOrderNumber;

    /** Creates a new instance of DebtorsDetailedAgingLineDS */
    public DebtorsDetailedAgingLineDS() {

    }

    public String getTransactionType() {
        return transactionType;
    }

    public void setTransactionType(String transactionType) {
        this.transactionType = transactionType;
    }

    public String getTransactionRef() {
        return transactionRef;
    }

    public void setTransactionRef(String transactionRef) {
        this.transactionRef = transactionRef;
    }

    public Date getTransactionDate() {
        return transactionDate;
    }

    public void setTransactionDate(Date transactionDate) {
        this.transactionDate = transactionDate;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public BigDecimal getBalance() {
        return balance;
    }

    public void setBalance(BigDecimal balance) {
        this.balance = balance;
    }

    public long getTransRecordID() {
        return transRecordID;
    }

    public void setTransRecordID(long transRecordID) {
        this.transRecordID = transRecordID;
    }

    public Date getPaymentDueDate() {
        return paymentDueDate;
    }

    public void setPaymentDueDate(Date paymentDueDate) {
        this.paymentDueDate = paymentDueDate;
    }

    public String getCustomerOrderNumber() {
        return customerOrderNumber;
    }

    public void setCustomerOrderNumber(String customerOrderNumber) {
        this.customerOrderNumber = customerOrderNumber;
    }
}

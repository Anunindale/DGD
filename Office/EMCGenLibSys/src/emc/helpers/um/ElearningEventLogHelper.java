package emc.helpers.um;

/**
 *
 * @author kapeshi
 */
public class ElearningEventLogHelper {

    private String studentNumber;
    private String studymaterialAccessed;
    private String marksAccessed;
    private String uploadedAssignment;
    private String statementDetails;
    private String updatedDetails;

    public ElearningEventLogHelper() {
    }

    public String getStudymaterialAccessed() {
        return studymaterialAccessed;
    }

    public void setStudymaterialAccessed(String studymaterialAccessed) {
        this.studymaterialAccessed = studymaterialAccessed;
    }

    public String getStudentNumber() {
        return studentNumber;
    }

    public void setStudentNumber(String studentNumber) {
        this.studentNumber = studentNumber;
    }

    public String getMarksAccessed() {
        return marksAccessed;
    }

    public void setMarksAccessed(String marksAccessed) {
        this.marksAccessed = marksAccessed;
    }

    public String getUploadedAssignment() {
        return uploadedAssignment;
    }

    public void setUploadedAssignment(String uploadedAssignment) {
        this.uploadedAssignment = uploadedAssignment;
    }

    public String getStatementDetails() {
        return statementDetails;
    }

    public void setStatementDetails(String statementDetails) {
        this.statementDetails = statementDetails;
    }

    public String getUpdatedDetails() {
        return updatedDetails;
    }

    public void setUpdatedDetails(String updatedDetails) {
        this.updatedDetails = updatedDetails;
    }

}

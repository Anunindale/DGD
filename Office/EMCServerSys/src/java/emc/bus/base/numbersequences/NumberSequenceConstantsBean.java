@Stateless
public class NumberSequenceConstantsBean implements NumberSequenceConstantsLocal {

    /** Number used when a number sequence is exhausted */
    private final long NUMSEQFLAG = -999l;
    
    public NumberSequenceConstantsBean(){
        
    }
    public long getNumSeqFlag() {
        return this.NUMSEQFLAG;
    }
}

package emc.bus.workflow.activitiesmanager.resources;

import java.util.Date;

/**
 *
 * @author wikus
 */
public class WFMyActivitiesMessage {

    private String activityNum;
    private String description;
    private Date dueDate;
    private String manager;
    private String taks;
    private String employee;

    public WFMyActivitiesMessage(String activityNum, String description, Date dueDate, String manager, String taks, String employee) {
        this.activityNum = activityNum;
        this.description = description;
        this.dueDate = dueDate;
        this.manager = manager;
        this.taks = taks;
        this.employee = employee;
    }

    public String getActivityNum() {
        return activityNum;
    }

    public String getDescription() {
        return description;
    }

    public Date getDueDate() {
        return dueDate;
    }

    public String getEmployee() {
        return employee;
    }

    public String getManager() {
        return manager;
    }

    public String getTaks() {
        return taks;
    }
}

package emc.bus.inventory.batchconsolidation.resources;

import java.math.BigDecimal;

/**
 *
 * @author wikus
 */
public class ItemDimBatchHelper {

    private String batch;
    private String location;
    private String toBatch;
    private String toLocation;
    private BigDecimal avaliable;
    private BigDecimal reserved;

    public ItemDimBatchHelper(String batch, String location, BigDecimal avaliable, BigDecimal reserved) {
        this.batch = batch;
        this.location = location;
        this.avaliable = avaliable;
        this.reserved = reserved;
    }

    public String getBatch() {
        return batch;
    }

    public void setBatch(String batch) {
        this.batch = batch;
    }

    public BigDecimal getAvaliable() {
        return avaliable;
    }

    public void setAvaliable(BigDecimal avaliable) {
        this.avaliable = avaliable;
    }

    public BigDecimal getReserved() {
        return reserved;
    }

    public void setReserved(BigDecimal reserved) {
        this.reserved = reserved;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getToBatch() {
        return toBatch;
    }

    public void setToBatch(String toBatch) {
        this.toBatch = toBatch;
    }

    public String getToLocation() {
        return toLocation;
    }

    public void setToLocation(String toLocation) {
        this.toLocation = toLocation;
    }
}

package emc.reports.inventory.ageing;

import emc.entity.inventory.transactions.InventoryTransactions;

/**
 *
 * @author rico
 */
public class CloseDataHolder {
    private long recordId;
    private double qtyClosed;
    private InventoryTransactions trans;

    public CloseDataHolder(long recordId,double qtyClosed,InventoryTransactions trans){
        this.recordId = recordId;
        this.qtyClosed = qtyClosed;
        this.trans = trans;
    }
    /**
     * @return the recordId
     */
    public long getRecordId() {
        return recordId;
    }

    /**
     * @param recordId the recordId to set
     */
    public void setRecordId(long recordId) {
        this.recordId = recordId;
    }

    /**
     * @return the qtyClosed
     */
    public double getQtyClosed() {
        return qtyClosed;
    }

    /**
     * @param qtyClosed the qtyClosed to set
     */
    public void setQtyClosed(double qtyClosed) {
        this.qtyClosed = qtyClosed;
    }

    /**
     * @return the trans
     */
    public InventoryTransactions getTrans() {
        return trans;
    }

    /**
     * @param trans the trans to set
     */
    public void setTrans(InventoryTransactions trans) {
        this.trans = trans;
    }


}

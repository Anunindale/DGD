public class CargoCheckReportDS {

    private String cargoCheckNumber;
    private String unNumber;
    private String properShipping;
    private String packingGroup;
    private String quantity;
    private String allowed;
    private String description;
    private String placeCard;
    private String trecRequired;
    private String cargo;
    private String notes;
    private String date;
    private Boolean printReport;
    public CargoCheckReportDS(){}

    public Boolean getPrintReport() {
        return printReport;
    }

    public void setPrintReport(Boolean printReport) {
        this.printReport = printReport;
    }

    

    public String getCargoCheckNumber() {
        return cargoCheckNumber;
    }

    public void setCargoCheckNumber(String cargoCheckNumber) {
        this.cargoCheckNumber = cargoCheckNumber;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getPackingGroup() {
        return packingGroup;
    }

    public void setPackingGroup(String packingGroup) {
        this.packingGroup = packingGroup;
    }

    public String getPlaceCard() {
        return placeCard;
    }

    public void setPlaceCard(String placeCard) {
        this.placeCard = placeCard;
    }

    public String getProperShipping() {
        return properShipping;
    }

    public void setProperShipping(String properShipping) {
        this.properShipping = properShipping;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

  

    public String getUnNumber() {
        return unNumber;
    }

    public void setUnNumber(String unNumber) {
        this.unNumber = unNumber;
    }

    public String getAllowed() {
        return allowed;
    }

    public void setAllowed(String allowed) {
        this.allowed = allowed;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getTrecRequired() {
        return trecRequired;
    }

    public void setTrecRequired(String trecRequired) {
        this.trecRequired = trecRequired;
    }

    

}

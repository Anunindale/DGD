package emc.reports.sop.rollingsalesreport;

import java.math.BigDecimal;

/**
 *
 * @author wikus
 */
public class SOPRollingSalesReportDS {

    private String fromPeriod;
    private String toPeriod;
    private String periodId;
    private BigDecimal invoicedUnits = BigDecimal.ZERO;
    private BigDecimal invoicedValue = BigDecimal.ZERO;
    private BigDecimal tradingUnits = BigDecimal.ZERO;
    private BigDecimal tradingValue = BigDecimal.ZERO;

    public BigDecimal getInvoicedUnits() {
        return invoicedUnits;
    }

    public void setInvoicedUnits(BigDecimal invoicedUnits) {
        this.invoicedUnits = invoicedUnits;
    }

    public BigDecimal getInvoicedValue() {
        return invoicedValue;
    }

    public void setInvoicedValue(BigDecimal invoicedValue) {
        this.invoicedValue = invoicedValue;
    }

    public String getPeriodId() {
        return periodId;
    }

    public void setPeriodId(String periodId) {
        this.periodId = periodId;
    }

    public BigDecimal getTradingUnits() {
        return tradingUnits;
    }

    public void setTradingUnits(BigDecimal tradingUnits) {
        this.tradingUnits = tradingUnits;
    }

    public BigDecimal getTradingValue() {
        return tradingValue;
    }

    public void setTradingValue(BigDecimal tradingValue) {
        this.tradingValue = tradingValue;
    }

    public String getFromPeriod() {
        return fromPeriod;
    }

    public void setFromPeriod(String fromPeriod) {
        this.fromPeriod = fromPeriod;
    }

    public String getToPeriod() {
        return toPeriod;
    }

    public void setToPeriod(String toPeriod) {
        this.toPeriod = toPeriod;
    }
}

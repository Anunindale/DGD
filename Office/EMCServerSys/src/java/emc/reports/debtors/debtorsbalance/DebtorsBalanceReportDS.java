package emc.reports.debtors.debtorsbalance;

import java.math.BigDecimal;

/**
 * @description : Data source for the Debtors Balance report.
 *
 * @date        : 28 Jul 2010
 *
 * @author      : Riaan Nel
 *
 * @version     : 1.0
 */
public class DebtorsBalanceReportDS {

    private BigDecimal invoiceTotal = BigDecimal.ZERO;
    private BigDecimal creditNoteTotal = BigDecimal.ZERO;
    private BigDecimal paymentTotal = BigDecimal.ZERO;
    private BigDecimal settlementDiscountTotal = BigDecimal.ZERO;
    private BigDecimal debitJournalTotal = BigDecimal.ZERO;
    private BigDecimal creditJournalTotal = BigDecimal.ZERO;
    private BigDecimal rebateTotal = BigDecimal.ZERO;
    private BigDecimal interestTotal = BigDecimal.ZERO;
    
    private BigDecimal startDateBalance = BigDecimal.ZERO;
    private BigDecimal endDateBalance = BigDecimal.ZERO;
    
    /** Creates a new instance of DebtorsBalanceReportDS */
    public DebtorsBalanceReportDS() {

    }

    public BigDecimal getInvoiceTotal() {
        return invoiceTotal;
    }

    public void setInvoiceTotal(BigDecimal invoiceTotal) {
        this.invoiceTotal = invoiceTotal;
    }

    public BigDecimal getCreditNoteTotal() {
        return creditNoteTotal;
    }

    public void setCreditNoteTotal(BigDecimal creditNoteTotal) {
        this.creditNoteTotal = creditNoteTotal;
    }

    public BigDecimal getPaymentTotal() {
        return paymentTotal;
    }

    public void setPaymentTotal(BigDecimal paymentTotal) {
        this.paymentTotal = paymentTotal;
    }

    public BigDecimal getSettlementDiscountTotal() {
        return settlementDiscountTotal;
    }

    public void setSettlementDiscountTotal(BigDecimal settlementDiscountTotal) {
        this.settlementDiscountTotal = settlementDiscountTotal;
    }

    public BigDecimal getDebitJournalTotal() {
        return debitJournalTotal;
    }

    public void setDebitJournalTotal(BigDecimal debitJournalTotal) {
        this.debitJournalTotal = debitJournalTotal;
    }

    public BigDecimal getCreditJournalTotal() {
        return creditJournalTotal;
    }

    public void setCreditJournalTotal(BigDecimal creditJournalTotal) {
        this.creditJournalTotal = creditJournalTotal;
    }

    public BigDecimal getRebateTotal() {
        return rebateTotal;
    }

    public void setRebateTotal(BigDecimal rebateTotal) {
        this.rebateTotal = rebateTotal;
    }

    public BigDecimal getStartDateBalance() {
        return startDateBalance;
    }

    public void setStartDateBalance(BigDecimal startDateBalance) {
        this.startDateBalance = startDateBalance;
    }

    public BigDecimal getEndDateBalance() {
        return endDateBalance;
    }

    public void setEndDateBalance(BigDecimal endDateBalance) {
        this.endDateBalance = endDateBalance;
    }

    public BigDecimal getInterestTotal() {
        return interestTotal;
    }

    public void setInterestTotal(BigDecimal interestTotal) {
        this.interestTotal = interestTotal;
    }
}

package emc.menus.sop.menuitems.display;

import emc.enums.enumMenuItems;
import emc.enums.enumMenuTypes;
import emc.forms.sop.display.customeractivity.CustomerActivityForm;
import emc.framework.EMCMenuItem;

/**
 * @description : Menu item for the customer activity form.
 *
 * @date        : 07 Jul 2010
 *
 * @author      : Riaan Nel
 *
 * @version     : 1.0
 */
public class CustomerActivityMenu extends EMCMenuItem {

    /** Creates a new instance of CustomerActivityMenu */
    public CustomerActivityMenu() {
        this.setMenuItemName("Customer Activity");
        this.setClassPath(CustomerActivityForm.class.getName());
        this.setMenuItemType(enumMenuItems.DISPLAY);
        this.setToolTipText("View the customer activity form.");
        this.setDoNotOpenForm(true);
    }
}
